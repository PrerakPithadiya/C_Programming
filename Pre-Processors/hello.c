#include <stdio.h> // Include the standard input/output header file

int main() {
    printf("Hello, world!\n"); // Print "Hello, world!" to the console
    return 0; // Indicate successful execution of the program
}

// - `#include <stdio.h>`: This line includes the standard input/output header file (`stdio.h`), which provides functions like `printf()` for printing to the console.

// - `int main() { ... }`: This is the main function of the program. It serves as the entry point of the program, where execution begins.

// - `printf("Hello, world!\n");`: This line uses the `printf()` function to print the string "Hello, world!" to the console. The `\n` is a special character that represents a newline, causing the cursor to move to the next line after printing the message.

// - `return 0;`: This line indicates that the program has executed successfully and returns an exit status of 0 to the operating system. This conventionally indicates success in C programs.
